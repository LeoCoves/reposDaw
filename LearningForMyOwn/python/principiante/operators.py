# Operaciones de Python
print("Hola" >= "Python")
print(3 > 4)
print(3 == 4)
print(3 != 4)
print("Hola" <= "Python")
print("Hola" > "Zola") #No ordena por caracteres
print("Hola" < "Zola") #Ordena por orden alfabetico ASCII

print("------")
#Para contar caracteres
print(len("Hola") > len("Zola"))
print("----")
#Operadores Logicos
print(3 > 4 and "Hola" > "Python") 
